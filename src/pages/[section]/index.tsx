import Navbar from '@/components/Navbar'
import React, { useEffect } from 'react'
import Footer from '@/components/Footer'
import Head from 'next/head'
import hljs from 'highlight.js'
import 'highlight.js/styles/github-dark.css'
import { getSectionIndex, getSections } from 'lib/sections'

export async function getStaticPaths() {
    const sections = await getSections();
    const paths = sections.map(section => ({
        params: { section }
    }))
    return { paths, fallback: false }
}

export async function getStaticProps({ params }: any) {
    const index = await getSectionIndex(params.section)
    return {
        props: { ...index }
    }
}

const Section = ({ title, content }: any) => {
    useEffect(() => {
        hljs.highlightAll()
    }, [])
    return (
        <>
            <Head>
                <title>{title}</title>
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <main>
                <Navbar />
                <section className="flex justify-center mt-[100px]">
                    <article className='prose dark:prose-invert w-[80%]' dangerouslySetInnerHTML={{ __html: content }}>
                    </article>
                </section>
            </main>
            <Footer />
        </>
    )
}

export default Section
