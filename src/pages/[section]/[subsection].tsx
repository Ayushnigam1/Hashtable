import Navbar from "@/components/Navbar";
import React from "react";
import Footer from "@/components/Footer";
import Head from "next/head";
import { getSections, getSubsectionPost, getSubSections } from "lib/sections";
import { BreadCrumbs } from "@/components/BreadCrumbs";

export async function getStaticPaths() {
    const sections = await getSections()
    const allPaths: any = []
    sections.forEach(async section => {
        const subSections = await getSubSections(section)
        subSections?.forEach(subsection => {
            allPaths.push({ params: { section, subsection } })
        })
    })
    return {
        paths: allPaths,
        fallback: false
    }
}

export async function getStaticProps({ params }: any) {
    const content = await getSubsectionPost(params.section, params.subsection)

    return {
        props: {
            ...content
        }
    }
}

const Subsection = ({ title, content }: any) => {
    return (
        <>
            <Head>
                <title>{title}</title>
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <main>
                <Navbar />
                <section className="mt-[100px] max-w-[65ch] m-auto">
                    <BreadCrumbs />
                    <article className='prose dark:prose-invert w-[80%]' dangerouslySetInnerHTML={{ __html: content }}>
                    </article>
                </section>
            </main>
            <Footer />
        </>
    )
}

export default Subsection;
